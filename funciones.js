// estas son las distintas formas de declarar una funcion
 



//function sencilla sin variaable
function sumaDos(a) {
    console.log(a + 2);
    
}

sumaDos (5);


//con variable
var sumaTres = function (b) {
    console.log(b + 3)
};
sumaTres (5);



// function de flecha 
var sumaCuatro = (c) => {
    console.log(c + 4)
};

sumaCuatro (5);